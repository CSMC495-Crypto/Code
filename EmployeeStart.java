	private void accountSearchButtonActionPerformed(
			java.awt.event.ActionEvent evt) {// GEN-FIRST:event_accountSearchButtonActionPerformed

		String accountNumber = accountSearchTextField.getText();
		String accountInfo = "";
		accountInfo = dao.getCustomerAccount(accountNumber);
		if (!accountInfo.equals("")) {
			// show AccountInfo
			AccountInfo ai = new AccountInfo();
			// parse accountInfo and populate Account balance Account Type
			// Account Number
			Scanner stdin = new Scanner(accountInfo);
			String balance = stdin.next();
			String type = stdin.next();
			String number = stdin.next();
			ai.setTopAccountInfo(balance, type, number);

			ai.setVisible(true);
		} else {
			// pop-up window "Account not found"

			JOptionPane.showMessageDialog((Component) null,
					"Account not found.", "alert", JOptionPane.OK_OPTION);
		}

		accountSearchTextField.setText("");
	}// GEN-LAST:event_accountSearchButtonActionPerformed

