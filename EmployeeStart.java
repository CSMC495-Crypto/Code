	private void accountsTableMousePressed(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_accountsTableMousePressed
		int col = accountsTable.columnAtPoint(evt.getPoint());
		int row = accountsTable.rowAtPoint(evt.getPoint());
		try {
			if (col == 0) {
				String accountNumber = accountsTable.getModel()
						.getValueAt(row, 0).toString();
				String accountInfo = "";
				accountInfo = dao.getCustomerAccount(accountNumber);
				if (!accountInfo.equals("")) {
					// show AccountInfo
					AccountInfo ai = new AccountInfo();
					// parse accountInfo and populate Account balance Account
					// Type Account Number
					Scanner stdin = new Scanner(accountInfo);
					ai.setTopAccountInfo(stdin.next(), stdin.next(),
							stdin.next());

					ai.setVisible(true);
				} else {
					// pop-up window "Account not found"

					JOptionPane.showMessageDialog((Component) null,
							"Account not found.", "alert",
							JOptionPane.OK_OPTION);
				}
			} // end if
		} // end try
		catch (NullPointerException npe) {
		}
	}// GEN-LAST:event_accountsTableMousePressed

	private void nameSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_nameSearchButtonActionPerformed
		String customerInfo = "";
		customerInfo = dao.getCustomerInformation(
				firstNameSearchTextField.getText(),
				nameSearchTextField.getText());
		Scanner stdin = new Scanner(customerInfo);

		if (customerInfo.equals("Error: Empty argument value.")) {
			// pop-up window "Account not found"

			JOptionPane.showMessageDialog((Component) null,
					"Enter customer's name.", "alert", JOptionPane.OK_OPTION);
		}

		else if (customerInfo.equals("Error: No such customer")) {
			// pop-up window "Account not found"

			JOptionPane.showMessageDialog((Component) null,
					"Customer not found.", "alert", JOptionPane.OK_OPTION);

		}

		else {

			// parse customerInfo populate form below with customers info
			firstNameTextField.setText(stdin.nextLine());
			lastNameTextField.setText(stdin.nextLine());
			phoneNumberTextField.setText(stdin.nextLine());
			addressTextField.setText(stdin.nextLine());
			cityTextField.setText(stdin.nextLine());
			stateTextField.setText(stdin.nextLine());
			zipCodeTextField.setText(stdin.nextLine());

			int i = 0;
			// accountsTable
			while (stdin.hasNext()) {
				for (int j = 0; j < 4; j++) {
					accountsTable.getModel().setValueAt(stdin.next(), i, j);
				} // end for
				i++;

			} // end while
		}

		firstNameSearchTextField.setText("");
		nameSearchTextField.setText("");

	}// GEN-LAST:event_nameSearchButtonActionPerformed

